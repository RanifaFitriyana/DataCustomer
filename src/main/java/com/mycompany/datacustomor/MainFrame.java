    private void loadCustomerData() {
        List<DataCustomor> customers = customerDAO.getAllCustomers();
        // Clear existing rows
        tableModel.setRowCount(0);
        // Add data rows
        for (DataCustomor customer : customers) {
            tableModel.addRow(new Object[]{customer.getId(), customer.getName(), customer.getEmail()});
        }
    }

