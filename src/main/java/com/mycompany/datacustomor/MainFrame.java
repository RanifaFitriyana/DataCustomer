    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String name = nameField.getText();
        String email = emailField.getText();

        if (!name.isEmpty() && !email.isEmpty()) {
            // Create DataCustomor object
            DataCustomor newCustomer = new DataCustomor(null, name, email);

            // Add to MongoDB
            customerDAO.addCustomer(newCustomer);

            // Add to JTable
            loadCustomerData();

            // Clear input fields
            nameField.setText("");
            emailField.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Name and email cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int selectedRow = table.getSelectedRow();

    if (selectedRow >= 0) {
        String customerId = (String) tableModel.getValueAt(selectedRow, 0);  // Ambil ID customer yang terpilih
        customerDAO.deleteCustomer(customerId);

        // Remove row from table
        tableModel.removeRow(selectedRow);
    } else {
        JOptionPane.showMessageDialog(this, "Please select a customer to delete", "Error", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        int selectedRow = table.getSelectedRow();

    if (selectedRow >= 0) {
        String customerId = (String) tableModel.getValueAt(selectedRow, 0);
        String newName = nameField.getText();
        String newEmail = emailField.getText();

        if (!newName.isEmpty() && !newEmail.isEmpty()) {
            DataCustomor updatedCustomer = new DataCustomor(customerId, newName, newEmail);
            customerDAO.updateCustomer(updatedCustomer);

            // Update row in JTable
            tableModel.setValueAt(newName, selectedRow, 1);
            tableModel.setValueAt(newEmail, selectedRow, 2);
        } else {
            JOptionPane.showMessageDialog(this, "Name and email cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a customer to update", "Error", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_updateButtonActionPerformed

